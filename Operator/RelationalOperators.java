public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println("Relational Operators");
        int a = 30;
        int b = 50;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
